# Promp the user for a radius and return the area

# Define variable[pi] as float=3.1415
pi = 3.1415

# Define variable[r] as input[please give the radius] and convert to flaot
r = float(input("please input radius\n\t>>>"))

# Define func[area] as pi * r**2
area = pi * r**2

# Message["A circle of radius {r} units has an area of {area} square units" ]
print("A circle of radius {} units has an area of {} square units.".format(r,area))


# Promp user for TEMP in fahrenheit and return the equivalent in celcius

# Define var[f] as float.input("Input temperature in Fahrenheit")
f = float(input("Input temperature in Fahrenheit\n\t>>>"))
# Define var[c] as (f - 32) * (5/9)
c = (f - 32) * (5/9)
# Message[{f} Fahrenheit is equivalent to {c} Celcius]
print("{} Fahrenheit is equivalent to {} Celcius".format(f,c))


