# Four people want four slices of pizza each, each pizza is cut into six slices.
# What is the minimum number of pizzas they must buy? Use floor devision.

# Define var[total_slices] as 4*4
total_slices = 4*4
# Define var[pizzas_needed] as (total_slices // 6) + 1
pizzas_needed = (total_slices // 6) + 1
# Define var[rem_slices] as total_slices % 6
rem_slices = (pizzas_needed * 6) - total_slices
# Message["Four people will need to buy {pizzas_needed} pizzas, with {rem_slices} slices remaining"]
print("Four people will need to buy {} pizzas, with {} remaining slices".format(pizzas_needed, rem_slices))